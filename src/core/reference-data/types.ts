/**
 * Типы Reference Data Engine — будущего единого движка справочных данных
 * платформы Noventra Core.
 *
 * На этом этапе движок — минимальный каркас: регистрация списков и чтение
 * их элементов. Он НЕ дублирует «Единые справочники» (src/data/dictionaries.ts,
 * CRUD-модуль с формами для пользователя) — это отдельный внутренний слой
 * ядра, через который любой будущий механизм платформы (в первую очередь
 * Requirement Matrix Engine) получает списки значений (профессии, должности,
 * гражданства, объекты и т.д.), не хардкодя их у себя.
 *
 * Сейчас списки заполняются заглушками (stubLists.ts). Когда появится
 * полноценный Reference Data Engine с реальными источниками данных (в т.ч.
 * синхронизацией с «Едиными справочниками»), достаточно будет заменить
 * реализацию registerList()/getList() — потребители (Requirement Matrix Engine
 * и другие) не изменятся, так как работают только через этот контракт.
 */

/** Идентификатор справочного списка (открытый строковый тип). */
export type ReferenceListId = string;

/** Элемент справочного списка. */
export interface ReferenceItem {
  id: string;
  label: string;
  description?: string;
}

/** Определение справочного списка целиком. */
export interface ReferenceListDefinition {
  id: ReferenceListId;
  labelKey: string;
  items: ReferenceItem[];
}
